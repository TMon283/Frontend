// Math.abs: trả về giá trị tuyệt đối
let number1 = Math.abs(-5.5); // 5.5
let number2 = Math.abs(10); // 10

// Math.ceil: làm tròn lên số nguyên gần nhất
let number3 = Math.ceil(4.4); // 5
let number4 = Math.ceil(5.8); // 6

// Math.floor: làm tròn xuống số nguyên gần nhất
let number5 = Math.floor(5.7); // 5
let number6 = Math.floor(6.1); // 6

// Math.round: làm tròn đến số nguyên gần nhất
let number7 = Math.round(10.1); // 10
let number8 = Math.round(20.6); // 21